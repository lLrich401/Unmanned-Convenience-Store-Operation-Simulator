def 줄():
    """구분선을 출력하는 함수"""
    print("=" * 72)


def 제목(글자):
    """큰 제목을 보기 좋게 출력하는 함수"""
    줄()
    print(글자.center(72))
    줄()


def 돈표시(돈):
    """숫자를 보기 좋게 바꾸는 함수"""
    return f"{돈:,}원"


def 입력숫자(안내문, 최소값=None, 최대값=None):
    """숫자를 안전하게 입력받는 함수"""
    while True:
        값 = input(안내문).strip()

        if 값.isdigit():
            숫자 = int(값)

            if 최소값 is not None and 숫자 < 최소값:
                print(f"{최소값} 이상의 숫자를 입력하세요.")
                continue

            if 최대값 is not None and 숫자 > 최대값:
                print(f"{최대값} 이하의 숫자를 입력하세요.")
                continue

            return 숫자

        print("숫자로 입력해 주세요.")


def 재고보기(재고, 가격표):
    """현재 재고와 가격을 함께 보여주는 함수"""
    제목("현재 재고와 가격")
    번호 = 1
    for 상품이름 in 재고:
        수량 = 재고[상품이름]
        가격 = 가격표[상품이름]
        print(f"{번호}. {상품이름:8} | 재고 {수량:2}개 | 가격 {돈표시(가격)}")
        번호 += 1
    줄()


def 상태보기(가게이름, 현재돈, 재고, 오늘점수, 홍보점수, 보안점수):
    """플레이어의 현재 상태를 출력하는 함수"""
    제목(f"{가게이름} 운영 현황")
    print(f"보유 자금 : {돈표시(현재돈)}")
    print(f"오늘 만족도 점수 : {오늘점수}")
    print(f"홍보 점수 : {홍보점수}")
    print(f"보안 점수 : {보안점수}")
    print("남은 재고")
    for 상품이름 in 재고:
        print(f"- {상품이름}: {재고[상품이름]}개")
    줄()


def 재고부족여부(재고):
    """재고가 너무 적은 상품이 있는지 확인"""
    부족목록 = []
    for 상품이름 in 재고:
        if 재고[상품이름] <= 2:
            부족목록.append(상품이름)
    return 부족목록


def 물건채우기(현재돈, 재고, 원가표):
    """직접 재고를 보충하는 메뉴"""
    제목("재고 채우기")
    상품목록 = list(재고.keys())

    for 번호 in range(len(상품목록)):
        상품이름 = 상품목록[번호]
        원가 = 원가표[상품이름]
        print(f"{번호 + 1}. {상품이름} - 1개 들여오기 비용 {돈표시(원가)}")
    print("0. 돌아가기")
    줄()

    선택 = 입력숫자("어떤 상품을 채울까요? ", 0, len(상품목록))
    if 선택 == 0:
        print("재고 보충을 취소했습니다.")
        return 현재돈

    상품이름 = 상품목록[선택 - 1]
    수량 = 입력숫자("몇 개를 들여올까요? ", 1, 20)
    총비용 = 원가표[상품이름] * 수량

    if 현재돈 < 총비용:
        print("돈이 부족해서 재고를 채울 수 없습니다.")
        print(f"필요한 돈: {돈표시(총비용)} / 현재 돈: {돈표시(현재돈)}")
        return 현재돈

    현재돈 -= 총비용
    재고[상품이름] += 수량

    print(f"{상품이름} {수량}개를 채웠습니다.")
    print(f"사용한 돈: {돈표시(총비용)}")
    print(f"남은 돈: {돈표시(현재돈)}")
    return 현재돈


def 가격조정(가격표):
    """상품 가격을 직접 조정하는 메뉴"""
    제목("가격 조정")
    상품목록 = list(가격표.keys())

    for 번호 in range(len(상품목록)):
        상품이름 = 상품목록[번호]
        print(f"{번호 + 1}. {상품이름} - 현재 가격 {돈표시(가격표[상품이름])}")
    print("0. 돌아가기")
    줄()

    선택 = 입력숫자("가격을 바꿀 상품 번호를 선택하세요: ", 0, len(상품목록))
    if 선택 == 0:
        print("가격 조정을 취소했습니다.")
        return

    상품이름 = 상품목록[선택 - 1]
    새가격 = 입력숫자(f"{상품이름}의 새 가격을 입력하세요: ", 500, 10000)
    이전가격 = 가격표[상품이름]
    가격표[상품이름] = 새가격

    print(f"{상품이름} 가격을 {돈표시(이전가격)}에서 {돈표시(새가격)}으로 바꿨습니다.")


def 홍보하기(현재돈, 홍보점수):
    """홍보를 하면 손님 수가 늘어날 수 있음"""
    제목("홍보하기")
    print("1. 전단지 돌리기 - 2,000원")
    print("2. 동네 광고판 - 5,000원")
    print("3. 인터넷 광고 - 8,000원")
    print("0. 하지 않기")
    줄()

    선택 = 입력숫자("홍보 방법을 고르세요: ", 0, 3)

    if 선택 == 0:
        print("오늘은 홍보를 하지 않았습니다.")
        return 현재돈, 홍보점수

    if 선택 == 1:
        비용 = 2000
        증가량 = 1
    elif 선택 == 2:
        비용 = 5000
        증가량 = 3
    else:
        비용 = 8000
        증가량 = 5

    if 현재돈 < 비용:
        print("돈이 부족해서 홍보를 할 수 없습니다.")
        return 현재돈, 홍보점수

    현재돈 -= 비용
    홍보점수 += 증가량
    print(f"홍보를 진행했습니다. 홍보 점수가 {증가량} 올랐습니다.")
    print(f"남은 돈: {돈표시(현재돈)}")
    return 현재돈, 홍보점수


