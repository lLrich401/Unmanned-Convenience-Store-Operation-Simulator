def 하루진행(가게이름, 현재돈, 재고, 가격표, 원가표, 홍보점수, 보안점수):
    """하루 영업을 실제로 진행하는 핵심 함수"""

    총매출 = 0
    총판매수 = 0
    판매실패수 = 0
    도난횟수 = 0
    오늘점수 = 0
    일반손님수 = 0
    vip손님수 = 0
    진상손님수 = 0
    할인횟수 = 0
    보너스횟수 = 0

    제목(f"{가게이름} 영업 시작")

    # 홍보를 많이 할수록 손님 수가 늘어날 가능성이 큽니다.
    오늘손님수 = random.randint(4, 8) + 홍보점수
    if 오늘손님수 > 18:
        오늘손님수 = 18

    print(f"오늘 방문 예정 손님 수: {오늘손님수}명")
    print("문이 열렸습니다. 손님이 들어오기 시작합니다.")

    for 손님번호 in range(1, 오늘손님수 + 1):
        print()
        print("-" * 72)
        print(f"{손님번호}번째 손님이 들어왔습니다.")

        손님유형 = random.choice(["일반손님", "VIP손님", "진상손님"])

        if 손님유형 == "일반손님":
            일반손님수 += 1
            손님예산 = random.randint(2000, 9000)
            살개수 = random.randint(1, 3)
            print("평범한 일반손님입니다.")
        elif 손님유형 == "VIP손님":
            vip손님수 += 1
            손님예산 = random.randint(7000, 18000)
            살개수 = random.randint(2, 4)
            print("VIP손님이 왔습니다. 많이 살 가능성이 높습니다.")
        else:
            진상손님수 += 1
            손님예산 = random.randint(1000, 8000)
            살개수 = random.randint(1, 3)
            print("진상손님이 왔습니다. 문제가 생길 수도 있습니다.")

        print(f"손님 유형: {손님유형}")
        print(f"예상 예산: {돈표시(손님예산)}")
        print(f"예상 구매 수량: {살개수}개")

        for 구매순서 in range(1, 살개수 + 1):
            상품이름 = random.choice(list(가격표.keys()))
            상품가격 = 가격표[상품이름]

            print(f"  {구매순서}. 고른 상품: {상품이름} / 가격 {돈표시(상품가격)}")

            if 재고[상품이름] <= 0:
                판매실패수 += 1
                오늘점수 -= 1
                print("  재고가 없어서 판매에 실패했습니다.")
                continue

            # 가격이 너무 비싸면 구매를 포기할 수 있습니다.
            if 손님예산 < 상품가격:
                판매실패수 += 1
                print("  손님 돈이 부족해서 결제가 취소되었습니다.")
                continue

            # 비싼 가격 때문에 손님이 마음을 바꿀 수도 있습니다.
            if 상품가격 >= 원가표[상품이름] * 2 and random.randint(1, 100) <= 35:
                판매실패수 += 1
                오늘점수 -= 1
                print("  손님이 가격이 너무 비싸다고 생각하고 그냥 나갔습니다.")
                continue

            할인금액 = 0
            할인주사위 = random.randint(1, 100)

            if 할인주사위 <= 12:
                할인금액 = 상품가격 * 10 // 100
                할인횟수 += 1
                print("  할인 이벤트가 발생했습니다. 10% 할인됩니다.")
            elif 할인주사위 == 77:
                할인금액 = 상품가격 * 50 // 100
                할인횟수 += 1
                print("  초특가 행운 이벤트가 발생했습니다. 50% 할인됩니다.")

            최종가격 = 상품가격 - 할인금액

            # 보안 점수가 낮을수록 도난 확률이 올라갑니다.
            도난주사위 = random.randint(1, 100)
            기본도난확률 = 7
            if 손님유형 == "진상손님":
                기본도난확률 = 18

            실제도난확률 = 기본도난확률 - 보안점수
            if 실제도난확률 < 2:
                실제도난확률 = 2

            if 도난주사위 <= 실제도난확률:
                재고[상품이름] -= 1
                도난횟수 += 1
                판매실패수 += 1
                오늘점수 -= 2
                print("  도난 사건이 발생했습니다. 상품은 사라졌지만 돈은 받지 못했습니다.")
                continue

            # 정상 판매 처리
            재고[상품이름] -= 1
            현재돈 += 최종가격
            총매출 += 최종가격
            총판매수 += 1
            손님예산 -= 최종가격
            오늘점수 += 2

            print(f"  판매 성공. {상품이름}을(를) {돈표시(최종가격)}에 판매했습니다.")

            # VIP 보너스 팁 이벤트
            if 손님유형 == "VIP손님" and random.randint(1, 100) <= 20:
                팁 = random.randint(500, 2000)
                현재돈 += 팁
                총매출 += 팁
                보너스횟수 += 1
                오늘점수 += 1
                print(f"  VIP손님이 팁 {돈표시(팁)}을 남겼습니다.")

            # 일반 보너스 이벤트
            if random.randint(1, 100) == 25:
                보너스돈 = 1000
                현재돈 += 보너스돈
                총매출 += 보너스돈
                보너스횟수 += 1
                print("  본사 쿠폰 정산금 1,000원이 추가로 들어왔습니다.")

        if 손님유형 == "진상손님" and random.randint(1, 100) <= 25:
            오늘점수 -= 1
            print("손님이 매장을 어지럽히고 나갔습니다. 청소가 더 필요해 보입니다.")
        else:
            print("손님이 매장을 떠났습니다.")

        부족목록 = 재고부족여부(재고)
        if len(부족목록) > 0:
            print("재고가 부족한 상품:", ", ".join(부족목록))

        print(f"현재 보유 자금: {돈표시(현재돈)}")

        # 손님 사이에 플레이어가 직접 고르는 짧은 행동을 넣어 게임성을 높입니다.
        print()
        print("짧은 행동 선택")
        print("1. 그냥 계속 영업하기")
        print("2. 삼각김밥 2개 빠르게 채우기")
        print("3. 라면 2개 빠르게 채우기")
        print("4. 안내문 붙여 보안 +1 올리기 (1,000원)")
        선택 = 입력숫자("선택: ", 1, 4)

        if 선택 == 2:
            비용 = 원가표["삼각김밥"] * 2
            if 현재돈 >= 비용:
                현재돈 -= 비용
                재고["삼각김밥"] += 2
                print("삼각김밥 2개를 빠르게 채웠습니다.")
            else:
                print("돈이 부족해서 삼각김밥을 채우지 못했습니다.")
        elif 선택 == 3:
            비용 = 원가표["라면"] * 2
            if 현재돈 >= 비용:
                현재돈 -= 비용
                재고["라면"] += 2
                print("라면 2개를 빠르게 채웠습니다.")
            else:
                print("돈이 부족해서 라면을 채우지 못했습니다.")
        elif 선택 == 4:
            if 현재돈 >= 1000:
                현재돈 -= 1000
                보안점수 += 1
                print("안내문을 붙였습니다. 보안 점수가 1 올랐습니다.")
            else:
                print("돈이 부족해서 안내문을 준비하지 못했습니다.")

    제목("하루 마감 정산")

    전기세 = random.randint(3000, 6000)
    청소비 = random.randint(2000, 5000)
    수리비 = 0

    if random.randint(1, 100) <= 20:
        수리비 = random.randint(2000, 7000)
        print(f"기계 수리비가 발생했습니다: {돈표시(수리비)}")
    else:
        print("오늘은 큰 기계 고장이 없었습니다.")

    총비용 = 전기세 + 청소비 + 수리비
    현재돈 -= 총비용

    print(f"총 매출: {돈표시(총매출)}")
